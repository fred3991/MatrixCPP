	const auto nr = vect.size();					// Number of elements in vect.

	// Preallocate vectors of C.
	for (i = 0; i < nr; ++i) {
		C.push_back({});						// Empty vectors.
	}

	for (i = 0; i < nr; ++i)
	{
		C[i] += vect[i] + bias[i];	// Calculate element.	
	}
	return C;
