matrix
activationTanh(matrix A)
{
	matrix C;
	unsigned int i, j, k;

	const auto nr = A.size();					// Number of rows in C.
	const auto nc = A[0].size();				// Number of columns in C.
	const auto np = A[0].size();				// Number of pairs to sum.

	// Preallocate vectors of C.
	for (i = 0; i < nr; ++i) {
		C.push_back({});						// Empty vectors.
	}
	for (i = 0; i < nr; ++i) {
		for (j = 0; j < nc; ++j) {
			C[i].push_back(0.0);				// Add 0 element.
			for (k = 0; k < np; ++k) {
				C[i][j] += tanh(A[i][k]);	// Calculate element.
			}
		}
	}

	return C;
}


vector<double>
matrixByVector(matrix A, vector<double> vect)
{
	vector<double> C;

	double tempVal = 0.0;
	double s = 0.0;
	unsigned int i, j, k;

	const auto nr = A.size();					// Number of rows in C.
	const auto nc = A[0].size();				// Number of columns in C.
	const auto np = A[0].size();				// Number of pairs to sum.

	for (i = 0; i < nc; ++i)
	{
		C.push_back({});						// Empty vectors.
	}

	const auto columns = A.size();				// Number of pairs to sum.
	const auto rows = A[0].size();				// Number of pairs to sum.

	// цикл по роус надо внутри цикла по колс


	for (i = 0; i < rows; ++i)
	{
		for (j = 0; j < columns; ++j)
		{
			s = A[j][i] * vect[j];
			tempVal += s;
		}

		C[i] += tempVal;
		tempVal = 0.0;
	}
	return C;
}

